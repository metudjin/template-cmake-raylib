/*======================================================================================================================
    Include Statements
======================================================================================================================*/

#if defined(PLATFORM_WEB)
    #include <emscripten/emscripten.h>
#endif

#include "app_includes.hpp"

#include "application.hpp"


/*======================================================================================================================
    Defines & Macros
======================================================================================================================*/



/*======================================================================================================================
    Variables and Constants (Global)
======================================================================================================================*/



/*======================================================================================================================
    Function declarations
======================================================================================================================*/

void UpdateDrawFrame();


/*======================================================================================================================
    Class instantiation
======================================================================================================================*/

Application* application = nullptr;


/*======================================================================================================================
    Main Entry Point
======================================================================================================================*/

int main()
{
    /*==================================================================================================================
        Variables and Constants
    ==================================================================================================================*/

    const int screen_width_init = 1600;
    const int screen_height_init = 900;


    /*==================================================================================================================
        Initialization (Initialize, Initializing)
    ==================================================================================================================*/

    //SetConfigFlags(FLAG_WINDOW_RESIZABLE);
    InitWindow(screen_width_init, screen_height_init, "Game name");
    //SetWindowState(FLAG_WINDOW_RESIZABLE);
    SetExitKey(KEY_ESCAPE);


    application = new Application();


    /*==================================================================================================================
        Main Application Loop - Calls UpdateDrawFrame();
    ==================================================================================================================*/

    #if defined(PLATFORM_WEB)
        emscripten_set_main_loop(UpdateDrawFrame, 0, 1);    // 0 = infinite FPS, 60 = 60 FPS
    #else

    SetTargetFPS(60);
    while (!WindowShouldClose())
    {
        UpdateDrawFrame();
    }
    #endif


    /*==================================================================================================================
        Finalization (Finalize, Finalizing)
    ==================================================================================================================*/



    /*==================================================================================================================
        Close application
    ==================================================================================================================*/

    CloseWindow();

    delete application;

    return 0;
}


void UpdateDrawFrame()
{
    if (!application) return;

    application->Update();
    application->Draw();
}
